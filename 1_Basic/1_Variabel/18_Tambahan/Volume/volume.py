def volume_kubus(sisi):
    print ('volume kubus')
    return sisi * sisi * sisi

def volume_balok(panjang,lebar,tinggi):
    print ('volume balok')
    return panjang * lebar * tinggi

def volume_bola(jarijari):
    print ('volume bola')
    return (4/3) * 3.14 * (jarijari ** 3)

def volume_tabung(jarijari, tinggi):
    print ('volume tabung')
    return 3.14 * (jarijari ** 2) * tinggi

def volume_prisma_segitiga(alas,tinggialas, tinggi):
    print ('volume prisma segitiga')
    return (1/2) * alas * tinggialas * tinggi